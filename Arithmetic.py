# Arithmetic Operations

print(1 + 1) # Addition (+)
print(2 - 1) # Subtraction (-)
print(2 * 2) # Multiplication (*)
print(10 - 2) #  Division (/)
print(3 ** 2)  # Exponential (**)
print(3 % 2)   # Modulus (%)
print(3 // 2)  # Floor division operator (//)

# Arithmetic Operations with Label 

